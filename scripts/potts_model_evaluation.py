ALPHABET_20 = "ACDEFGHIKLMNPQRSTVWY"


def parse_aligned_fasta_to_indices(
    aligned_fasta_path: str,
    alphabet: str = ALPHABET_20,
    include_gaps: bool = True,
) -> Tuple[np.ndarray, List[str]]:
    """
    Parse an aligned FASTA into an integer array of shape (num_sequences, L).
    
    Returns (indices, sequence_ids) where indices uses 0..19 for amino acids,
    optionally 20 for gaps (if include_gaps=True), and -1 for unknown residues.
    """
    records = list(SeqIO.parse(aligned_fasta_path, "fasta"))
    if len(records) == 0:
        raise ValueError(f"No sequences found in {aligned_fasta_path}")
    seq_ids = [r.id for r in records]
    seq_strs = [str(r.seq).upper() for r in records]
    L = len(seq_strs[0])
    for s in seq_strs:
        if len(s) != L:
            raise ValueError("All sequences must be the same aligned length")

    aa_to_index = {aa: i for i, aa in enumerate(alphabet)}
    if include_gaps:
        aa_to_index['-'] = len(alphabet)
        aa_to_index['.'] = len(alphabet)
    
    x = np.full((len(seq_strs), L), -1, dtype=np.int16)
    for n, s in enumerate(seq_strs):
        for i, ch in enumerate(s):
            x[n, i] = aa_to_index.get(ch, -1)
    return x, seq_ids


def fit_potts_model_meanfield(
    aligned_fasta_path: str,
    output_dir: str,
    lambda_reg: float = 0.01,
) -> Tuple[str, Dict]:
    """
    Fit a mean-field Potts model (Direct Coupling Analysis).
    
    This computes:
    - Single-site frequencies (fields h_i)
    - Pairwise frequencies (couplings J_ij)
    - Connected correlations using mean-field approximation
    
    Returns:
        (model_file_path, info_dict)
    """
    os.makedirs(output_dir, exist_ok=True)
    
    x, seq_ids = parse_aligned_fasta_to_indices(aligned_fasta_path, include_gaps=True)
    num_sequences, L = x.shape
    q = 21  # 20 amino acids + 1 gap
    
    # Filter out sequences with too many unknown residues
    valid_seq_mask = np.sum(x >= 0, axis=1) > (0.5 * L)
    x = x[valid_seq_mask]
    num_sequences = x.shape[0]
    
    if num_sequences < 10:
        raise ValueError(f"Too few valid sequences ({num_sequences}) after filtering")
    
    # Compute sequence weights (simple: all equal for now)
    # Advanced: could use sequence identity-based reweighting
    weights = np.ones(num_sequences) / num_sequences
    
    # Create one-hot encoding for vectorized computation (fully vectorized)
    # one_hot[n, i, a] = 1 if sequence n has amino acid a at position i, else 0
    one_hot = np.zeros((num_sequences, L, q), dtype=np.float32)
    valid_mask = (x >= 0)  # Boolean mask for valid residues
    seq_idx, pos_idx = np.where(valid_mask)
    one_hot[seq_idx, pos_idx, x[seq_idx, pos_idx]] = 1.0
    
    # Apply sequence weights
    weighted_one_hot = one_hot * weights[:, None, None]
    
    # Single-site frequencies (vectorized)
    f_i = weighted_one_hot.sum(axis=0)  # (L, q)
    
    # Add pseudocount (Laplace smoothing)
    f_i = (f_i + lambda_reg) / (1 + lambda_reg * q)
    
    # Pairwise frequencies (vectorized using einsum)
    # f_ij[i, j, a, b] = sum_n weights[n] * one_hot[n, i, a] * one_hot[n, j, b]
    print(f"  Computing pairwise frequencies (vectorized)...")
    f_ij = np.einsum('nia,njb,n->ijab', one_hot, one_hot, weights, optimize=True)
    
    # Add pseudocount
    f_ij = (f_ij + lambda_reg) / (1 + lambda_reg * q * q)
    
    # Connected correlations (covariance)
    C_ij = f_ij - f_i[:, None, :, None] * f_i[None, :, None, :]
    
    # Mean-field approximation: J_ij = -C_ij
    # (Proper DCA would invert the correlation matrix, but this is a good approximation)
    J_ij = -C_ij
    
    # Fields from single-site frequencies (log probabilities)
    h_i = np.log(f_i + 1e-10)
    
    model = {
        "h_i": h_i,
        "J_ij": J_ij,
        "f_i": f_i,
        "f_ij": f_ij,
        "C_ij": C_ij,
        "L": L,
        "q": q,
    }
    
    # Save model to file
    model_file = os.path.join(output_dir, "potts_model_params.pkl")
    with open(model_file, 'wb') as f:
        pickle.dump(model, f)
    
    info = {
        "num_sequences": num_sequences,
        "alignment_length": L,
        "method": "mean_field_dca",
        "lambda_reg": lambda_reg,
    }
    
    return model_file, info


def compute_sequence_energies(
    aligned_fasta_path: str,
    model_file: str,
) -> np.ndarray:
    """
    Compute Potts model energy for each sequence in an alignment (vectorized).
    
    Energy is computed as:
        E(sequence) = -sum_i h_i(s_i) - sum_{i<j} J_ij(s_i, s_j)
    
    Lower energy indicates higher probability under the model.
    
    Returns:
        Array of energies, one per sequence
    """
    x, seq_ids = parse_aligned_fasta_to_indices(aligned_fasta_path, include_gaps=True)
    num_sequences, L = x.shape
    
    # Load model
    with open(model_file, 'rb') as f:
        model = pickle.load(f)
    
    # Handle different model formats
    if isinstance(model, dict):
        h_i = model["h_i"]
        J_ij = model["J_ij"]
        q = model["q"]
    elif hasattr(model, 'h_i') and hasattr(model, 'J_ij'):
        h_i = model.h_i
        J_ij = model.J_ij
        q = h_i.shape[1]
    else:
        raise ValueError("Unknown model format")
    
    # Create one-hot encoding (vectorized)
    one_hot = np.zeros((num_sequences, L, q), dtype=np.float32)
    valid_mask = (x >= 0)
    seq_idx, pos_idx = np.where(valid_mask)
    one_hot[seq_idx, pos_idx, x[seq_idx, pos_idx]] = 1.0
    
    # Field energy: -sum_i h_i(s_i) for each sequence
    # one_hot[n, i, a] * h_i[i, a] gives h_i(s_i) for sequence n
    field_energies = -np.einsum('nia,ia->n', one_hot, h_i)
    
    # Coupling energy: -sum_{i,j} J_ij(s_i, s_j) for each sequence
    # Note: J_ij is symmetric and includes diagonal, so we sum over all i,j but divide by 2
    # Actually, for upper triangle only: sum_{i<j}
    coupling_energies = -0.5 * np.einsum('nia,njb,ijab->n', one_hot, one_hot, J_ij, optimize=True)
    
    energies = field_energies + coupling_energies
    
    return energies


def compute_log_likelihood(
    aligned_fasta_path: str,
    model_file: str,
) -> float:
    """
    Compute average log-likelihood of sequences under the Potts model.
    
    This is approximated as -energy (unnormalized), since computing the 
    partition function is intractable.
    
    Returns:
        Mean log-likelihood across sequences
    """
    energies = compute_sequence_energies(aligned_fasta_path, model_file)
    # Negative energy is proportional to log P(sequence) up to normalization
    return float(-np.mean(energies))


def compute_contact_prediction_scores(
    model_file: str,
) -> np.ndarray:
    """
    Compute contact prediction scores from Potts model couplings.
    
    Uses Frobenius norm of coupling matrix J_ij to predict contacts.
    This is the standard DCA contact prediction approach.
    
    Returns:
        (L, L) matrix of contact scores
    """
    with open(model_file, 'rb') as f:
        model = pickle.load(f)
    
    if isinstance(model, dict):
        J_ij = model["J_ij"]
        L = model["L"]
    elif hasattr(model, 'J_ij'):
        J_ij = model.J_ij
        L = J_ij.shape[0]
    else:
        raise ValueError("Unknown model format")
    
    # Frobenius norm of coupling matrix for each pair
    contact_scores = np.zeros((L, L))
    for i in range(L):
        for j in range(L):
            if i != j:
                # Frobenius norm: sqrt(sum(J_ij[i,j,:,:]^2))
                contact_scores[i, j] = np.sqrt(np.sum(J_ij[i, j, :, :] ** 2))
    
    # Apply Average Product Correction (APC) to remove background
    mean_i = contact_scores.mean(axis=1)
    mean_j = contact_scores.mean(axis=0)
    mean_all = contact_scores.mean()
    
    contact_scores_apc = contact_scores - (mean_i[:, None] * mean_j[None, :]) / mean_all
    
    return contact_scores_apc


def make_combined_aligned_fastas(natural_fasta: str, synthetic_fasta: str) -> Tuple[str, str, str]:
    """
    Create a combined FASTA (natural followed by synthetic), run a single MAFFT alignment,
    then split the aligned combined MSA back into aligned natural and aligned synthetic FASTAs.

    Returns (natural_aligned_path, synthetic_aligned_path, combined_aligned_path).
    """
    align_dir = os.path.join(os.path.dirname(synthetic_fasta), "alignments")
    os.makedirs(align_dir, exist_ok=True)

    base = os.path.basename(synthetic_fasta).replace(".fasta", "")
    combined_fasta_path = os.path.join(align_dir, f"{base}_combined.fasta")
    combined_aln_path = os.path.join(align_dir, f"{base}_combined_aln.fasta")
    nat_aln_path = os.path.join(align_dir, f"{base}_natural_aln.fasta")
    syn_aln_path = os.path.join(align_dir, f"{base}_synthetic_aln.fasta")

    # Write combined FASTA if missing and remember split index
    if not os.path.exists(combined_fasta_path):
        nat_records = list(SeqIO.parse(natural_fasta, "fasta"))
        # Natural FASTAs may already be aligned; strip gap characters before combining
        for r in nat_records:
            # Remove common gap characters from natural sequences
            r.seq = Seq(str(r.seq).replace("-", "").replace(".", ""))

        syn_records = list(SeqIO.parse(synthetic_fasta, "fasta"))
        with open(combined_fasta_path, "w") as fout:
            SeqIO.write(nat_records + syn_records, fout, "fasta")
        nat_count = len(nat_records)
    else:
        # If combined exists, derive nat_count from natural file
        nat_count = sum(1 for _ in SeqIO.parse(natural_fasta, "fasta"))

    # Align combined once
    if not os.path.exists(combined_aln_path):
        align_with_mafft(combined_fasta_path, combined_aln_path)

    # Split aligned combined into aligned natural/synthetic
    need_split = (not os.path.exists(nat_aln_path)) or (not os.path.exists(syn_aln_path))
    if need_split:
        aligned_records = list(SeqIO.parse(combined_aln_path, "fasta"))
        nat_aligned = aligned_records[:nat_count]
        syn_aligned = aligned_records[nat_count:]
        with open(nat_aln_path, "w") as f_nat:
            SeqIO.write(nat_aligned, f_nat, "fasta")
        with open(syn_aln_path, "w") as f_syn:
            SeqIO.write(syn_aligned, f_syn, "fasta")

    return nat_aln_path, syn_aln_path, combined_aln_path


def evaluate_potts_model(
    natural_aligned_fasta: str,
    synthetic_aligned_fasta: str,
    output_dir: str,
) -> Dict:
    """
    Fit Potts model on natural MSA and evaluate on synthetic MSA.
    Uses mean-field DCA implementation.
    
    Returns dictionary with evaluation metrics:
    - natural_log_likelihood: average log-likelihood on natural MSA
    - synthetic_log_likelihood: average log-likelihood on synthetic MSA
    - natural_mean_energy: mean energy on natural MSA
    - synthetic_mean_energy: mean energy on synthetic MSA
    - natural_std_energy: std of energy on natural MSA
    - synthetic_std_energy: std of energy on synthetic MSA
    - energy_correlation: correlation between natural and synthetic energy distributions
    """
    os.makedirs(output_dir, exist_ok=True)
    
    print(f"Fitting Potts model on natural MSA: {natural_aligned_fasta}")
    model_file, train_info = fit_potts_model_meanfield(natural_aligned_fasta, output_dir)
    
    print(f"Computing energies on natural MSA...")
    natural_energies = compute_sequence_energies(natural_aligned_fasta, model_file)
    
    print(f"Computing energies on synthetic MSA: {synthetic_aligned_fasta}")
    synthetic_energies = compute_sequence_energies(synthetic_aligned_fasta, model_file)
    
    # Compute log-likelihoods (negative mean energy)
    natural_log_likelihood = -np.mean(natural_energies)
    synthetic_log_likelihood = -np.mean(synthetic_energies)
    
    # Energy statistics
    results = {
        "natural_log_likelihood": float(natural_log_likelihood),
        "synthetic_log_likelihood": float(synthetic_log_likelihood),
        "log_likelihood_ratio": float(synthetic_log_likelihood / natural_log_likelihood) if natural_log_likelihood != 0 else float('nan'),
        "natural_mean_energy": float(np.mean(natural_energies)),
        "synthetic_mean_energy": float(np.mean(synthetic_energies)),
        "natural_std_energy": float(np.std(natural_energies)),
        "synthetic_std_energy": float(np.std(synthetic_energies)),
        "natural_median_energy": float(np.median(natural_energies)),
        "synthetic_median_energy": float(np.median(synthetic_energies)),
        "num_natural_sequences": len(natural_energies),
        "num_synthetic_sequences": len(synthetic_energies),
    }
    
    # Add training info
    results.update(train_info)
    
    # Compute energy distribution correlation
    # Match distribution sizes for correlation
    min_size = min(len(natural_energies), len(synthetic_energies))
    if min_size > 1:
        nat_sample = np.random.choice(natural_energies, size=min_size, replace=False)
        syn_sample = np.random.choice(synthetic_energies, size=min_size, replace=False)
        # Sort both to compare distributions
        nat_sample.sort()
        syn_sample.sort()
        energy_corr = np.corrcoef(nat_sample, syn_sample)[0, 1]
        results["energy_distribution_correlation"] = float(energy_corr)
    else:
        results["energy_distribution_correlation"] = float('nan')
    
    # Save energies
    np.save(os.path.join(output_dir, "natural_energies_meanfield.npy"), natural_energies)
    np.save(os.path.join(output_dir, "synthetic_energies_meanfield.npy"), synthetic_energies)
    
    return results


