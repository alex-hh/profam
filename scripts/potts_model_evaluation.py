def compare_potts_implementations(
    meanfield_results: Dict,
    evcouplings_results: Dict,
) -> Dict:
    """
    Compare results from mean-field DCA and EVcouplings implementations.
    
    Returns a dictionary with comparison metrics.
    """
    if "error" in evcouplings_results:
        return {"comparison_available": False, "reason": evcouplings_results["error"]}
    
    comparison = {
        "comparison_available": True,
        # Energy differences
        "natural_mean_energy_diff": abs(
            meanfield_results["natural_mean_energy"] - evcouplings_results["natural_mean_energy"]
        ),
        "synthetic_mean_energy_diff": abs(
            meanfield_results["synthetic_mean_energy"] - evcouplings_results["synthetic_mean_energy"]
        ),
        # Log-likelihood differences
        "natural_ll_diff": abs(
            meanfield_results["natural_log_likelihood"] - evcouplings_results["natural_log_likelihood"]
        ),
        "synthetic_ll_diff": abs(
            meanfield_results["synthetic_log_likelihood"] - evcouplings_results["synthetic_log_likelihood"]
        ),
        # Correlation differences
        "energy_corr_diff": abs(
            meanfield_results["energy_distribution_correlation"] - evcouplings_results["energy_distribution_correlation"]
        ),
        # Ratio agreement
        "ll_ratio_diff": abs(
            meanfield_results["log_likelihood_ratio"] - evcouplings_results["log_likelihood_ratio"]
        ),
    }
    
    return comparison


