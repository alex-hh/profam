"""
Potts Model Evaluation for Protein MSAs

This script implements Direct Coupling Analysis (DCA) using Potts models to evaluate 
synthetic protein sequences against natural MSAs. It provides TWO implementations:

1. **Custom Mean-Field DCA**: Simple, self-contained implementation
   - Computes single-site and pairwise amino acid frequencies
   - Uses mean-field approximation: J_ij = -C_ij (connected correlations)
   - No external dependencies beyond numpy

2. **EVcouplings Library**: Professional implementation from Debbie Marks lab
   - Proper sequence reweighting based on identity threshold
   - More sophisticated parameter estimation
   - Standard tool in the field

Outputs:
--------
The script generates a CSV with results from BOTH methods (prefixed for clarity):
- mf_* : Custom mean-field DCA results
- evc_* : EVcouplings library results  
- comp_* : Comparison metrics between the two methods

Key Metrics:
- natural/synthetic_log_likelihood: Higher = better fit (unnormalized)
- natural/synthetic_mean_energy: Lower = more favorable under model
- log_likelihood_ratio: synthetic_ll / natural_ll (closer to 1.0 = better)
- energy_distribution_correlation: Similarity of energy distributions
- num_effective_sequences: Effective sample size after reweighting (evc only)

Comparison Metrics:
- *_diff: Absolute differences between methods
- If methods agree well, differences should be small

Usage:
------
    python potts_model_evaluation.py

The script processes EC (Enzyme Commission) validation datasets, fitting Potts models
to natural MSAs and evaluating synthetic sequences generated by the ProFam model.
Same datasets as evaluate_covariance.py for consistency.
"""

