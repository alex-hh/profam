def fit_potts_model_evcouplings(
    aligned_fasta_path: str,
    output_dir: str,
    identity_threshold: float = 0.8,
    lambda_J: float = 0.01,
    lambda_h: float = 0.01,
) -> Tuple[str, Dict]:
    """
    Fit a Potts model using EVcouplings library.
    
    This uses proper pseudo-likelihood maximization and sequence reweighting.
    
    Args:
        aligned_fasta_path: Path to aligned FASTA file
        output_dir: Directory to save model outputs
        identity_threshold: Sequence identity threshold for reweighting (0.8 = 80%)
        lambda_J: L2 regularization for couplings
        lambda_h: L2 regularization for fields
        
    Returns:
        (model_file_path, info_dict)
    """
    if not EVCOUPLINGS_AVAILABLE:
        raise ImportError("evcouplings library is not available")
    
    os.makedirs(output_dir, exist_ok=True)
    
    # Load alignment
    alignment = Alignment.from_file(aligned_fasta_path, format="fasta")
    
    # Compute sequence weights based on identity threshold
    alignment.set_weights(identity_threshold=identity_threshold)
    
    print(f"  Alignment: {alignment.N} sequences, {alignment.L} positions")
    print(f"  Effective sequence count after reweighting: {alignment.N_eff:.1f}")
    
    # Fit coupling model using mean-field DCA
    # Note: EVcouplings has multiple backends (plmc, mean_field, etc.)
    # We'll use their mean_field implementation for consistency
    from evcouplings.couplings import mean_field  # type: ignore
    
    # Compute couplings
    model = mean_field(
        alignment,
        lambda_J=lambda_J,
        lambda_h=lambda_h,
    )
    
    # Save model
    model_file = os.path.join(output_dir, "potts_model_evcouplings.pkl")
    with open(model_file, 'wb') as f:
        pickle.dump(model, f)
    
    info = {
        "num_sequences": alignment.N,
        "num_effective_sequences": float(alignment.N_eff),
        "alignment_length": alignment.L,
        "method": "evcouplings_mean_field",
        "identity_threshold": identity_threshold,
        "lambda_J": lambda_J,
        "lambda_h": lambda_h,
    }
    
    return model_file, info


def compute_sequence_energies_evcouplings(
    aligned_fasta_path: str,
    model_file: str,
) -> np.ndarray:
    """
    Compute Potts model energy using EVcouplings model (vectorized).
    
    Returns:
        Array of energies, one per sequence
    """
    if not EVCOUPLINGS_AVAILABLE:
        raise ImportError("evcouplings library is not available")
    
    # Load model
    with open(model_file, 'rb') as f:
        model = pickle.load(f)
    
    # Load alignment
    alignment = Alignment.from_file(aligned_fasta_path, format="fasta")
    
    # The EVcouplings model should have methods to compute energies
    # Access the coupling parameters
    if hasattr(model, 'J_ij'):
        J_ij = model.J_ij
        h_i = model.h_i
    else:
        # Fallback: extract from model attributes
        raise ValueError("EVcouplings model format not recognized")
    
    # Get numeric sequence matrix
    x = alignment.matrix  # (N, L) array of amino acid indices
    num_sequences, L = x.shape
    q = h_i.shape[1]
    
    # Create one-hot encoding (vectorized)
    one_hot = np.zeros((num_sequences, L, q), dtype=np.float32)
    valid_mask = (x >= 0)
    seq_idx, pos_idx = np.where(valid_mask)
    one_hot[seq_idx, pos_idx, x[seq_idx, pos_idx]] = 1.0
    
    # Field energy: -sum_i h_i(s_i) for each sequence
    field_energies = -np.einsum('nia,ia->n', one_hot, h_i)
    
    # Coupling energy: -sum_{i,j} J_ij(s_i, s_j) for each sequence
    coupling_energies = -0.5 * np.einsum('nia,njb,ijab->n', one_hot, one_hot, J_ij, optimize=True)
    
    energies = field_energies + coupling_energies
    
    return energies


